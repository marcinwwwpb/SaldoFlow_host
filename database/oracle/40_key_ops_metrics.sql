CREATE TABLE SF_KEY_OPERATION_METRIC (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  METRIC_DATE DATE NOT NULL,
  OPERATION_CODE VARCHAR2(64) NOT NULL,
  QUANTITY NUMBER DEFAULT 0 NOT NULL,
  CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL
);

CREATE UNIQUE INDEX SF_UX_KEYOPS_DATE_CODE ON SF_KEY_OPERATION_METRIC(METRIC_DATE, OPERATION_CODE);

CREATE OR REPLACE VIEW SF_V_MONTHLY_DATA_LOAD AS
SELECT 'FINANSE_OPERACJA' AS TABLE_NAME, TRUNC(DATA, 'MM') AS PERIOD_START, COUNT(*) AS ROW_COUNT
  FROM FINANSE_OPERACJA
 GROUP BY TRUNC(DATA, 'MM')
UNION ALL
SELECT 'FIRMA_FAKTURAKOSZTOWA' AS TABLE_NAME, TRUNC(DATA_ZAKUPU, 'MM') AS PERIOD_START, COUNT(*) AS ROW_COUNT
  FROM FIRMA_FAKTURAKOSZTOWA
 GROUP BY TRUNC(DATA_ZAKUPU, 'MM')
UNION ALL
SELECT 'FIRMA_IMPORTDEMONA' AS TABLE_NAME, TRUNC(CREATED_AT, 'MM') AS PERIOD_START, COUNT(*) AS ROW_COUNT
  FROM FIRMA_IMPORTDEMONA
 GROUP BY TRUNC(CREATED_AT, 'MM');
