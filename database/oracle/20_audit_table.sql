-- Tabela audytowa i metryki zmian na poziomie Oracle.

CREATE TABLE SF_DB_CHANGE_LOG (
  ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  CHANGED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
  ACTOR_DB_USER VARCHAR2(128),
  APP_USER_ID VARCHAR2(64),
  ROLE_CODE VARCHAR2(32),
  TABLE_NAME VARCHAR2(128) NOT NULL,
  ROW_PK VARCHAR2(128) NOT NULL,
  OPERATION VARCHAR2(10) NOT NULL,
  CHANGED_FIELDS CLOB,
  BEFORE_STATE CLOB,
  AFTER_STATE CLOB
);

CREATE INDEX SF_DB_CHANGE_LOG_I1 ON SF_DB_CHANGE_LOG (TABLE_NAME, CHANGED_AT);
CREATE INDEX SF_DB_CHANGE_LOG_I2 ON SF_DB_CHANGE_LOG (APP_USER_ID, CHANGED_AT);
CREATE INDEX SF_DB_CHANGE_LOG_I3 ON SF_DB_CHANGE_LOG (ROLE_CODE, CHANGED_AT);
