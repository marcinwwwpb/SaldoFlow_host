  SF_ADD_POLICY_IF_EXISTS('FIRMA_IMPORTDEMONA', 'SF_VPD_IMPORTS', 'SF_POLICY_OWNER_USER');
  SF_ADD_POLICY_IF_EXISTS('FINANSE_KONTODOMOWE', 'SF_VPD_HOME_ACCOUNTS', 'SF_POLICY_OWNER_UZYTKOWNIK');
